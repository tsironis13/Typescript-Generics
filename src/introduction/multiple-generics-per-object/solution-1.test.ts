import { expect, it } from "vitest";
import { Equal, Expect } from "../../common/type-utils";

const returnBothOfWhatPassingIn = <X, Z>(params: { a: X; b: Z }) => {
  return {
    first: params.a,
    second: params.b,
  };
};

it("Should return an object where a -> first and b -> second", () => {
  const result = returnBothOfWhatPassingIn({
    a: "a",
    b: 1,
  });

  expect(result).toEqual({
    first: "a",
    second: 1,
  });

  type test1 = Expect<
    Equal<
      typeof result,
      {
        first: string;
        second: number;
      }
    >
  >;
});
